public class Day15 {
    public static void main(String[] args) {
        // Deklarasi variabel
        int a = 10;
        int b = 4;

        //operasi aritmatika
        int tambah = a + b;
        int kurang = a - b;

        //Output hasil
        System.out.println(" Nilai a = " + a);
        System.out.println(" Nilai b = " + b);
        System.out.println(" Hasil penjumlahan (a + b) " + tambah);
        System.out.println(" Hasil pengurangan (a - b) " + kurang);
        
    }
    
}
