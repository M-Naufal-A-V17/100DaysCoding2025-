public class Day4 {
    public static void main(String[] args) {
        // \n untuk baris baru
       System.out.println("hai vall \nthank you") ;
       // \t untuk tab
       System.out.println("nama: \tvall");
       // \" untuk tanda kutip
       System.out.println("\"halo\"");
       // \' tanda kutip tunggal
       System.out.println("\'nice vall\'");
       // \menampilkan karakter backslash
       System.out.println("\\hello");
       // \r carriage return
       System.out.println("hello \rworld");
       // \b backspace
       System.out.println("hello\b!");
    }
}