public class Day9{
    public static void main(String[] args) {
        // Deklarasi
        String buah;
        int jumlah;

        // Inisialisasi
        buah = "Jeruk";
        jumlah = 5;

        System.out.println("Buah awal : " + buah);
        System.out.println("Jumlah awal : " + jumlah);

        // Update variabel
        buah = "Anggur";
        jumlah = 12;

        System.out.println("Buah update : " + buah);
        System.out.println("Jumlah update : " + jumlah);
         

    }
}