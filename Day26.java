public class Day26 {
    public static void main(String[] args) {
        int x = 10; // penugasan biasa
        System.out.println("Nilai awal x = " + x);

        x += 5; // sama dengan x = x + 5
        System.out.println("Setelah x += 5, nilai x = " + x);

        x -= 3; // sama dengan x = x - 3
        System.out.println("Setelah x -= 3, nilai x = " + x);

        x *= 2; // sama dengan x = x * 2
        System.out.println("Setelah x *= 2, nilai x = " + x);

        x /= 4; // sama dengan x = x / 4
        System.out.println("Setelah x /= 4, nilai x = " + x);

        x %= 3; // sama dengan x = x % 3
        System.out.println("Setelah x %= 3, nilai x = " + x);
    }
    
}
