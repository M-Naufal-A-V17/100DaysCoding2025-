public class Day5{
    public static void main(String[] args){
        // tipe data byte
       byte umur = 18;
       System.out.println("umur (byte): " + umur);
       // tipe data int
       int jumlahpenduduk  = 1800000000;
       System.out.println("jumlah penduduk sebuah negara (int): " + jumlahpenduduk);
       // tipe data long
       long jarakpulau = 9480730472580800L; 
       System.out.println("jarak pulau A ke pulau B (long): " + jarakpulau);
       // tipe data short
       short jumlahsebuahbangunan = 513;
       System.out.println("jumlah bangunan di sebuah kota (short): " + jumlahsebuahbangunan);


    }
}